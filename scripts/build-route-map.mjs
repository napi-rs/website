// Generates lib/i18n/route-map.gen.ts (committed, imported by the edge
// middleware + the sitemap). One-shot manual/CI generator like build-nav.mjs —
// NOT a Vite plugin and NOT run by `void deploy`. Regenerate after adding,
// removing, or translating any page (and after editing a blog `date:`).
//
// Regenerate with: node scripts/build-route-map.mjs
import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { join, dirname, resolve } from 'node:path'
import { fileURLToPath, pathToFileURL } from 'node:url'
import { execSync } from 'node:child_process'
import { walkPages, fileToRoute } from './generate-sitemap.mjs'
import { splitLocale } from '../lib/docs/locale.ts'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')
const pagesDir = join(root, 'pages')
const outFile = join(root, 'lib', 'i18n', 'route-map.gen.ts')

/** Group public routes by neutral path -> sorted unique locales. Pure. */
export function buildRouteMap(routes) {
  const acc = {}
  for (const route of routes) {
    const [locale, rest] = splitLocale(route)
    const neutral = rest ? `/${rest}` : '/'
    ;(acc[neutral] ??= new Set()).add(locale)
  }
  const out = {}
  for (const key of Object.keys(acc).sort()) out[key] = [...acc[key]].sort()
  return out
}

/** Pull a `date:` string from a blog `.md` (frontmatter may follow a <script>). Pure. */
export function parseBlogDate(src) {
  const m = src.match(
    /^\s*---[\s\S]*?\ndate:\s*['"]?([0-9]{4}-[0-9]{2}-[0-9]{2})['"]?/m,
  )
  return m ? m[1] : null
}

function generate() {
  const files = walkPages(pagesDir)
  const routes = []
  for (const rel of files) {
    const route = fileToRoute(rel)
    if (route !== null) routes.push(route)
  }
  const routeMap = buildRouteMap(routes)

  const blogDates = {}
  for (const rel of files) {
    if (!rel.endsWith('.md') || !rel.includes('/blog/')) continue
    const route = fileToRoute(rel)
    if (route === null) continue
    const [, rest] = splitLocale(route)
    const neutral = rest ? `/${rest}` : '/'
    const date = parseBlogDate(readFileSync(join(pagesDir, rel), 'utf8'))
    if (date) blogDates[neutral] = date
  }

  const ts = `// GENERATED by scripts/build-route-map.mjs — DO NOT EDIT.
// Which locales really serve each route (for hreflang + sitemap alternates) and
// blog publish dates (for BlogPosting JSON-LD). Data-only + edge-safe.
// Regenerate with: node scripts/build-route-map.mjs
import type { Locale } from '../nav/index.ts'

export const routeMap: Record<string, Locale[]> = ${JSON.stringify(routeMap, null, 2)}

export const blogDates: Record<string, string> = ${JSON.stringify(blogDates, null, 2)}
`
  writeFileSync(outFile, ts, 'utf8')
  try {
    execSync(`vp fmt "${outFile}" --write`, { cwd: root, stdio: 'pipe' })
  } catch {
    // vp not on PATH; pre-commit hook will format it.
  }
  console.log(
    `route-map: ${Object.keys(routeMap).length} routes, ${Object.keys(blogDates).length} blog dates -> ${outFile}`,
  )
}

const cliEntry = process.argv[1]
if (cliEntry && import.meta.url === pathToFileURL(cliEntry).href) generate()
