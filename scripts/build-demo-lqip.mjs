// scripts/build-demo-lqip.mjs
//
// Generates the low-quality image placeholder (LQIP) for the in-browser image
// playground's RESULT slot (the transformed <img ref={transformedRef}> in
// components/transform-image/_Demo.tsx) and writes it to
// components/transform-image/lqip.gen.ts.
//
// Why a build artifact: the old napi.rs (Next.js) component seeded that slot
// with `nasaImage.blurDataURL` — a tiny Next-generated LQIP that renders as a
// blurry placeholder signalling "no result yet." The Vite migration dropped
// blurDataURL (an image import now resolves to a plain URL string), so the slot
// briefly showed a misleading full-quality copy of the original. We restore the
// LQIP by transcoding the source PNG here with @napi-rs/image (the same native
// library the demo showcases): a tiny blurred WebP, base64-encoded into a
// `data:image/webp;base64,…` URI that ships inline in the client bundle.
//
// Tuning: ~32px wide (aspect preserved → 32x21 for the 4928x3279 source), a
// light Gaussian blur, and a low WebP quality keep the data URI tiny (a few
// hundred bytes) while the browser's smooth upscale reads as the napi.rs blur.
//
// Regenerate after changing the source image or params: `node
// scripts/build-demo-lqip.mjs` (the output is committed; this is a one-shot
// generator like build-demo-code.mjs / build-nav.mjs).

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { Transformer } from '@napi-rs/image'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')
const imageDir = join(root, 'components', 'transform-image')
const inFile = join(imageDir, 'nasa-4928x3279.png')
const outFile = join(imageDir, 'lqip.gen.ts')

// LQIP params: small enough for a tiny inline data URI, blurred enough that the
// browser upscale reads as a placeholder rather than a low-res thumbnail.
const WIDTH = 32
const SIGMA = 1.0
const QUALITY = 60

const png = readFileSync(inFile)
const webp = new Transformer(new Uint8Array(png))
  .resize(WIDTH)
  .blur(SIGMA)
  .webpSync(QUALITY)

const dataUri = `data:image/webp;base64,${webp.toString('base64')}`

const banner = `// GENERATED by scripts/build-demo-lqip.mjs — DO NOT EDIT.
// Tiny blurred WebP LQIP for the image playground's result slot (produced from
// nasa-4928x3279.png via @napi-rs/image), replacing the old Next blurDataURL.
// Regenerate with: node scripts/build-demo-lqip.mjs\n\n`

// The data URI (only base64 + data-URI chars — no quotes/backslashes) always
// exceeds the print width, so emit it pre-wrapped in the exact shape Oxfmt
// produces: single-quoted, broken after `=`. This keeps `node
// scripts/build-demo-lqip.mjs` idempotent with `vp fmt` (regenerate stays
// format-clean without a follow-up --write).
const body = banner + `export const NASA_LQIP =\n  '${dataUri}'\n`

writeFileSync(outFile, body)
console.log(`wrote ${outFile}`)
console.log(`  params: width=${WIDTH} sigma=${SIGMA} quality=${QUALITY}`)
console.log(`  webp:     ${webp.length} bytes`)
console.log(`  data URI: ${dataUri.length} bytes`)
